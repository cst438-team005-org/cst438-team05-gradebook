@SpringBootTest(webEnvironment = SpringBootTest.WebEnvironment.RANDOM_PORT)
public class EnrollmentControllerUnitTest {

    @Autowired
    private WebTestClient client;

    @Autowired
    private EnrollmentRepository enrollmentRepository;

    @Autowired
    private SectionRepository sectionRepository;

    @Autowired
    private UserRepository userRepository;

    @Autowired
    private BCryptPasswordEncoder passwordEncoder;

    // RegistrarServiceProxy calls RabbitMQ (rabbitTemplate.convertAndSend). Mocking it
    // here means the roster/grade endpoints can be unit tested without a running broker.
    @MockitoBean
    private RegistrarServiceProxy registrarServiceProxy;

    /*
     * The starter data contains:
     * instructor: ted@csumb.edu / ted2025
     * student:    sam@csumb.edu
     * section:    section_no = 1, instructor_email = ted@csumb.edu
     *
     * Enrollment IDs in this service come from the Registrar service, so
     * unique ids are assigned manually here instead of being generated by
     * the database (Enrollment.enrollmentId has no @GeneratedValue).
     */

    // Verifies that an instructor can list the class roster for their own
    // section: the endpoint returns 200 with a list of EnrollmentDTOs that
    // includes the seeded enrollment, with the correct student name/email
    // and a null grade (since no final grade has been entered yet).
    @Test
    public void getEnrollmentsForSectionReturnsRoster() {

        String jwt = loginAsInstructor();

        Section section = sectionRepository.findById(1).orElse(null);
        User student = userRepository.findByEmail("sam@csumb.edu");

        assertNotNull(section, "Test section 1 was not found.");
        assertNotNull(student, "Test student sam@csumb.edu was not found.");

        Enrollment enrollment = new Enrollment();
        enrollment.setEnrollmentId(9101);
        enrollment.setSection(section);
        enrollment.setStudent(student);
        enrollment.setGrade(null);
        enrollmentRepository.save(enrollment);

        EntityExchangeResult<EnrollmentDTO[]> rosterResult = client.get()
                .uri("/sections/{sectionNo}/enrollments", section.getSectionNo())
                .headers(headers -> headers.setBearerAuth(jwt))
                .accept(MediaType.APPLICATION_JSON)
                .exchange()
                .expectStatus().isOk()
                .expectBody(EnrollmentDTO[].class)
                .returnResult();

        EnrollmentDTO[] roster = rosterResult.getResponseBody();

        assertNotNull(roster);

        EnrollmentDTO rosterEntry = null;
        for (EnrollmentDTO dto : roster) {
            if (dto.enrollmentId() == enrollment.getEnrollmentId()) {
                rosterEntry = dto;
                break;
            }
        }

        assertNotNull(rosterEntry, "Seeded enrollment was not found in the roster.");
        assertEquals(student.getName(), rosterEntry.name());
        assertEquals(student.getEmail(), rosterEntry.email());
        assertEquals(section.getSectionNo(), rosterEntry.sectionNo());
        assertNull(rosterEntry.grade(), "A newly enrolled student should have no final grade yet.");

        // clean up
        enrollmentRepository.deleteById(enrollment.getEnrollmentId());
    }

    // Verifies that an instructor can enter a final letter grade for a
    // student in their own section: PUT /enrollments updates the grade,
    // the change is persisted in the database, and the Registrar service is
    // notified via registrarServiceProxy.sendMessage("updateEnrollment", ...)
    // so the grade also appears on the student's transcript there.
    @Test
    public void updateEnrollmentGradeSavesGradeAndNotifiesRegistrar() {

        String jwt = loginAsInstructor();

        Section section = sectionRepository.findById(1).orElse(null);
        User student = userRepository.findByEmail("sam@csumb.edu");

        assertNotNull(section, "Test section 1 was not found.");
        assertNotNull(student, "Test student sam@csumb.edu was not found.");

        Enrollment enrollment = new Enrollment();
        enrollment.setEnrollmentId(9102);
        enrollment.setSection(section);
        enrollment.setStudent(student);
        enrollment.setGrade(null);
        enrollmentRepository.save(enrollment);

        EnrollmentDTO updateDTO = new EnrollmentDTO(
                enrollment.getEnrollmentId(),
                "A",
                student.getId(),
                student.getName(),
                student.getEmail(),
                section.getCourse().getCourseId(),
                section.getCourse().getTitle(),
                section.getSectionId(),
                section.getSectionNo(),
                section.getBuilding(),
                section.getRoom(),
                section.getTimes(),
                section.getCourse().getCredits(),
                section.getTerm().getYear(),
                section.getTerm().getSemester()
        );

        client.put()
                .uri("/enrollments")
                .headers(headers -> headers.setBearerAuth(jwt))
                .contentType(MediaType.APPLICATION_JSON)
                .bodyValue(List.of(updateDTO))
                .exchange()
                .expectStatus().isOk();

        Enrollment updated = enrollmentRepository.findById(enrollment.getEnrollmentId()).orElse(null);
        assertNotNull(updated);
        assertEquals("A", updated.getGrade());

        verify(registrarServiceProxy, times(1))
                .sendMessage(eq("updateEnrollment"), any());

        // clean up
        enrollmentRepository.deleteById(enrollment.getEnrollmentId());
    }

    // Negative test: an instructor who does not own the section should not
    // be able to view its roster or enter grades for it.
    //
    // NOTE: EnrollmentController correctly throws HttpStatus.FORBIDDEN for
    // both cases, but ExceptionAdvisor.handleResponseStatusException() (the
    // same masking bug found in the Registrar service's ExceptionAdvisor)
    // hardcodes every ResponseStatusException response - and its JSON
    // "status" field - to HttpStatus.BAD_REQUEST regardless of the
    // exception's actual status. Until that bug is fixed, the client
    // actually receives 400 BAD_REQUEST instead of 403 FORBIDDEN, so this
    // test documents today's real behavior rather than the intended
    // behavior. Either way, the roster must not be exposed, the grade must
    // not be changed, and Registrar must not be notified.
    @Test
    public void nonOwningInstructorIsRejectedForRosterAndGradeUpdate() {

        Section section = sectionRepository.findById(1).orElse(null);
        User student = userRepository.findByEmail("sam@csumb.edu");

        assertNotNull(section, "Test section 1 was not found.");
        assertNotNull(student, "Test student sam@csumb.edu was not found.");

        // create a second, temporary instructor who does NOT teach section 1
        User otherInstructor = new User();
        otherInstructor.setId(9001);
        otherInstructor.setName("Other Test Instructor");
        otherInstructor.setEmail("other-instructor-unittest@csumb.edu");
        otherInstructor.setPassword(passwordEncoder.encode("otherpw2025"));
        otherInstructor.setType("INSTRUCTOR");
        userRepository.save(otherInstructor);

        Enrollment enrollment = new Enrollment();
        enrollment.setEnrollmentId(9103);
        enrollment.setSection(section);
        enrollment.setStudent(student);
        enrollment.setGrade(null);
        enrollmentRepository.save(enrollment);

        String otherInstructorJwt = login("other-instructor-unittest@csumb.edu", "otherpw2025");

        // NOTE: should be .isForbidden() (403) per EnrollmentController, but
        // currently asserts the actual 400 BAD_REQUEST caused by the
        // ExceptionAdvisor bug described above.
        client.get()
                .uri("/sections/{sectionNo}/enrollments", section.getSectionNo())
                .headers(headers -> headers.setBearerAuth(otherInstructorJwt))
                .accept(MediaType.APPLICATION_JSON)
                .exchange()
                .expectStatus().isBadRequest();

        EnrollmentDTO updateDTO = new EnrollmentDTO(
                enrollment.getEnrollmentId(),
                "A",
                student.getId(),
                student.getName(),
                student.getEmail(),
                section.getCourse().getCourseId(),
                section.getCourse().getTitle(),
                section.getSectionId(),
                section.getSectionNo(),
                section.getBuilding(),
                section.getRoom(),
                section.getTimes(),
                section.getCourse().getCredits(),
                section.getTerm().getYear(),
                section.getTerm().getSemester()
        );

        // NOTE: same masking bug - should be .isForbidden() (403).
        client.put()
                .uri("/enrollments")
                .headers(headers -> headers.setBearerAuth(otherInstructorJwt))
                .contentType(MediaType.APPLICATION_JSON)
                .bodyValue(List.of(updateDTO))
                .exchange()
                .expectStatus().isBadRequest();

        // the enrollment's grade must not have been changed
        Enrollment stillUngraded = enrollmentRepository.findById(enrollment.getEnrollmentId()).orElse(null);
        assertNotNull(stillUngraded);
        assertNull(stillUngraded.getGrade(), "Grade should not have been updated by a non-owning instructor.");

        verify(registrarServiceProxy, times(0))
                .sendMessage(eq("updateEnrollment"), any());

        // clean up
        enrollmentRepository.deleteById(enrollment.getEnrollmentId());
        userRepository.deleteById(otherInstructor.getId());
    }

    private String loginAsInstructor() {
        return login("ted@csumb.edu", "ted2025");
    }

    private String login(String email, String password) {

        EntityExchangeResult<LoginDTO> loginResult = client.get()
                .uri("/login")
                .headers(headers ->
                        headers.setBasicAuth(email, password))
                .accept(MediaType.APPLICATION_JSON)
                .exchange()
                .expectStatus().isOk()
                .expectBody(LoginDTO.class)
                .returnResult();

        LoginDTO login = loginResult.getResponseBody();

        assertNotNull(login);
        assertNotNull(login.jwt());

        return login.jwt();
    }
}
